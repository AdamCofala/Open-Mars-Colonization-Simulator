{
}

Tile& Map::getTile(int x, int y) const
{
    if (x < 0 || x >= width || y < 0 || y >= height) {
        throw std::out_of_range("Tile coordinates out of range.");
    }
    return const_cast<Tile&>(tiles[y * width + x]);
}

int Map::getWidth() const
{
	return width;
}

int Map::getHeight() const
{
	return height;
}
